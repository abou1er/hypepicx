             <h2>hypEpic - Présentation </h2>


                <p>Le projet que j’ai réalisé qui est donc « hypEpic», un projet qui répond à un désir qui me passionné. Consulter des images que l’on ne retrouve pas de partout mais de qualité en plus rare voir d'être inédit. J’ai cherché à réaliser quelque chose qui me ressemblé. 
                    <br>  Aujourd’hui il existe des applications comme instagram, pinterest, ou encore tumblr qui existe et qui ont su se créer une grande communauté.  </p>
<p>
J’ai donc voulu créer pour le parti utilisateur : </p>
<ul>
    <li>page d’accueil pour tout le monde où l’on peut s’inscrire et se connecter </li>
    <li>page de consultation de l’ensemble du contenu proposé et pouvoir les afficher par catégorie ou faire une recherche en tapant le titre de l’image.</li>
    <li>une page ou une fois qu’on clique sur l’image celle-ci s’affiche et où il sera possible de laisser son avis en la commentant.</li>
    <li>une page paramètre pour modifier son pseudo de profil et son mot de passe. </li>
    <li>une page pour voir l’ensemble des commentaires qu’on à pu poster dans tous le site et pouvoir supprimer un message si l’utilisateur le souhaite.</li>
</ul>







<p>
Pour l’administrateur en plus des pages déjà sitées : </p>
<ul>
    <li>une page pour insérer une « Pic » en fonction de sa catégorie.</li>
    <li>une page pour supprimer une « Pic ».</li>
    <li>une page pour ajouter un membre ou un admin.</li>
    <li>une page pour supprimer un membre.</li>
    <li>une page permettant de supprimer un message peut importe par qui il a était posté.</li>
</ul>


